package ia;

public class Tools {
	
	public static char intToCharSp(int i){
		char ch;
		
		switch(i) {
		case 1 :
			ch = 'A'; break;
		case 2 :
			ch = 'B'; break;
		case 3 :
			ch = 'C'; break;
		case 4 :
			ch = 'D'; break;
		case 5 :
			ch = 'E'; break;
		case 6 :
			ch = 'F'; break;
		case 7 :
			ch = 'G'; break;
		case 8 :
			ch = 'H'; break;
		case 9 :
			ch = 'I'; break;
		default :
			ch = 'A'; break;
		}	
		
		return ch;
	}
}
